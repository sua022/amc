package cn.hugoz.amc.parser;

public class MethodArgs {
	private TypeEntity argsType;

	public TypeEntity getArgsType() {
		return argsType;
	}

	public void setArgsType(TypeEntity argsType) {
		this.argsType = argsType;
	}

	public String getArgsName() {
		return argsName;
	}

	public void setArgsName(String argsName) {
		this.argsName = argsName;
	}

	private String argsName;
}
